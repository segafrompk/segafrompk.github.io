let trenutni = galerijaPodaci;
let trenutniIme = 'galerija';

function nadjiParentObjekat (nodeName) {
    if (Object.prototype.hasOwnProperty.call(galerijaPodaci, nodeName)) {
        trenutniIme = 'galerija'
        return galerijaPodaci;
    } else {
        var galerijaNavigacija = Object.entries(galerijaPodaci);
        for (let i = 1; i < galerijaNavigacija.length; i++) {
            if (Object.prototype.hasOwnProperty.call(galerijaNavigacija[i][1], nodeName)) {
                trenutniIme = galerijaNavigacija[i][0];    
                return galerijaNavigacija[i][1];
            }
        }
    }
}

function idiNazad () {
    if (trenutniIme === 'galerija') {
        console.log('Ne možete više ići nazad.');
    } else {
        trenutni = nadjiParentObjekat(trenutniIme);
        napraviGaleriju(trenutni);
    }
}

function otvoriChildObjekat (imeChildObjekta) {
    trenutni = trenutni[imeChildObjekta];
    trenutniIme = imeChildObjekta;
    napraviGaleriju(trenutni);
}

function napraviGaleriju (trenutniPregled) {
    var galerija = document.querySelector('.gallery');
    if (galerija.querySelector('.row') != null) {
        galerija.removeChild(galerija.querySelector('.row'));
    }
    var galerijaRow = document.createElement('div');
    galerijaRow.className = 'row';
    var trenutniPregledLista = Object.entries(trenutniPregled)
    if (trenutniPregled.sadrziSlike === true) {
        for (var i = 1; i < trenutniPregledLista.length; i++) {
            galerijaRow.appendChild(elementGalerije(trenutniPregledLista[i], true));
        }
    } else {
        for (var j = 1; j < trenutniPregledLista.length; j++) {
            galerijaRow.appendChild(elementGalerije(trenutniPregledLista[j], false));
        }
    }
    galerija.appendChild(galerijaRow);
    poravnajTekst();
    baguetteBox.run('.gallery', {
        "overlayBackgroundColor": "rgba(0, 0, 0, 0.7)",
        "noScrollbars": "false",
        "animation": "slideIn"
      });
}

function elementGalerije (podaci, slika) {
    if (slika) {
        var element = document.createElement('div');
        var elementA = document.createElement('a');
        var innerImg = document.createElement('img');
        innerImg.className = 'element';
        innerImg.src = podaci[1].adresaSlike;
        if (podaci[1].opisSlike !== "") elementA.setAttribute('data-caption', podaci[1].opisSlike)
        elementA.href = podaci[1].adresaSlike;
        elementA.className = 'lightbox';
        element.className = 'col-sm-12 col-md-4';
        elementA.appendChild(innerImg);
        element.appendChild(elementA);
        return element;
    }
    else {
        var element = document.createElement('div');
        var unutrasnjiTekst = document.createElement('span');
        var dummy = document.createElement('div');
        dummy.className = 'dummy';
        unutrasnjiTekst.innerText = podaci[0];
        unutrasnjiTekst.className = 'tekstPrekoSlike'
        element.className = 'col-sm-12 col-md-4 folder overlay';
        element.style.backgroundImage = 'url(\'images/galerija/1.jpg\')';
        element.appendChild(unutrasnjiTekst);
        element.appendChild(dummy);
        element.addEventListener('click', x => otvoriChildObjekat(podaci[0]));
        return element;
    }
}
function poravnajTekst() {
    document.querySelectorAll('.tekstPrekoSlike').forEach(x => {
        x.style.top = `calc(50% - ${x.offsetHeight/2}px)`;
        x.style.left = `calc(50% - ${x.offsetWidth/2}px)`;
    })
}

napraviGaleriju(trenutni);
